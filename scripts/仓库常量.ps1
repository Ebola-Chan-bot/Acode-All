# 调试服务器端口必须由仓库内单一常量统一定义，避免启动脚本、元数据和构建注入各自漂移到不同端口，最终把旧地址打进 APK。这里集中收口后，任何偏离都应直接失败，而不是静默改用别的值。 仅调试用
$script:Acode仓库固定调试服务器端口 = 8092

function 获取仓库固定调试服务器端口 {
    return [int]$script:Acode仓库固定调试服务器端口
}

function 断言仓库固定调试服务器端口 {
    param(
        [int]$目标端口,
        [string]$来源 = "unknown"
    )

    $固定端口 = 获取仓库固定调试服务器端口
    if ([int]$目标端口 -ne $固定端口) {
        throw "检测到非仓库固定调试服务器端口: source=$来源 actual=$目标端口 expected=$固定端口"
    }
}